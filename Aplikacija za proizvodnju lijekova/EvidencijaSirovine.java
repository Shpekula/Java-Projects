private void popuniSirovine(){
    ArrayList<Sirovine>listaSirovina = sqlsirovine.listaSirovina();
    for(Sirovine sirovine: listaSirovina){
       cmbSirovine.addItem(sirovine);
    }
}

private void autoISirovine()
{
    ArrayList<Sirovine> listaSirovina = sqlsirovine.listaSirovina();
    int sifra = 0;
    for(Sirovine sirovine: listaSirovina)
    {
        sifra = sirovine.getSifraSirovine();
    }
    if (sifra == 0)
    {
        sifra = 1;
        txtSifraSirovine.setText(Integer.toString(sifra));
    }
    else
    {
        txtSifraSirovine.setText(Integer.toString(sifra + 1));
    }
}
