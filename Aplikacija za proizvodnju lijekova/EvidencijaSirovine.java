    private void btnSpremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpremiActionPerformed
        // TODO add your handling code here:
        int sifraSirovine = Integer.parseInt(txtSifraSirovine.getText());
        String nazivSirovine = txtNazivSirovine.getText();
        int kolicinaSirovine = Integer.parseInt(txtKolicinaSirovine.getText());
        
        Sirovine sirovine = new Sirovine();
        sirovine.setSifraSirovine(sifraSirovine);
        sirovine.setNazivSirovine(nazivSirovine);
        sirovine.setKolicinaSirovine(kolicinaSirovine);
        
        sqlsirovine.spremi(sirovine);
        
        sifraSirovine += 1;
        
        //txtSifraSirovine sifraSirovine;
        autoISirovine();
        txtKolicinaSirovine.setText("");
        txtNazivSirovine.setText("");
        
        //ovdje kreirati objekt i poslati putem SQL tablice, odnosno u bazu podataka
        
    }//GEN-LAST:event_btnSpremiActionPerformed

