    private void cmbProizvodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProizvodActionPerformed
        // TODO add your handling code here:
        GotovProizvod selProizvod = (GotovProizvod) cmbProizvod.getSelectedItem();
        
        txtSifraProizvoda.setText(Integer.toString(selProizvod.getSifraProizvoda()));
        txtNazivProizvoda.setText(selProizvod.getNazivProizvoda());
        txtKolicinaProizvoda.setText(Integer.toString(selProizvod.getKolicinaProizvoda()));
        
        int sifraProizvoda = selProizvod.getSifraProizvoda();
        int sifraPP = 0;
        int kolicinaZP = 0;
        
        ArrayList<Ambalaza> listaAmbalaze = sqlambalaza.listaAmbalaze();
        for(Ambalaza am: listaAmbalaze)
        {
            int sifraAmbalaze = am.getSifraAmbalaze();
            sifraPP = am.getSifraPP();
            int sifraProizvod = am.getSifraProizvoda();
            kolicinaZP = am.getKolicinaZP();
            
            if(sifraProizvoda == sifraProizvod)
            {
                txtSifraAmbalaze.setText(Integer.toString(sifraAmbalaze));
                txtSifraPP.setText(Integer.toString(sifraPP));
                txtPotrebnaKolicina.setText(Integer.toString(kolicinaZP));
            }
        }
        
        int kolicinaPolu = 0;
        
        ArrayList<Poluproizvod> listaPoluproizvoda = sqlpp.listaPoluproizvoda();
        for(Poluproizvod pp: listaPoluproizvoda)
        {
            int sifraPolu = pp.getSifraPP();
            String nazivPolu = pp.getNazivPP();
            kolicinaPolu = pp.getKolicinaPP();
            
            if(sifraPP == sifraPolu)
            {
                txtKolicinaPP.setText(Integer.toString(kolicinaPolu));
            }
        }
        
        int ukupnoZP = kolicinaPolu / kolicinaZP;
        txtUkupnoPakiranje.setText(Integer.toString(ukupnoZP));      
        
        
    }//GEN-LAST:event_cmbProizvodActionPerformed

