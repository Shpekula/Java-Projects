    private void btnPakirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPakirajActionPerformed
        // TODO add your handling code here:
        int sifraPakiranja = Integer.parseInt(txtSifraPakiranja.getText());
        int sifraAmbalaze = Integer.parseInt(txtSifraAmbalaze.getText());
        int kolicinaPakiranih = Integer.parseInt(txtUkupnoPakiranje.getText());
        String datumProizvodnje = txtDatum.getText();
        String rokValjanosti = txtRokValjanosti.getText();
        
        String rok = Datum.toString();
        
        Pakiranje pakiranje = new Pakiranje();
        pakiranje.setSifraPakiranja(sifraPakiranja);
        pakiranje.setSifraAmbalaze(sifraAmbalaze);
        pakiranje.setKolicinaPakiranih(kolicinaPakiranih);
        pakiranje.setDatumProizvodnje(datumProizvodnje);
        pakiranje.setRokValjanosti(rokValjanosti);
        sqlpakiranje.spremi(pakiranje);
        
        int sifraProizvoda = Integer.parseInt(txtSifraProizvoda.getText());
        int kolicinaProizvoda = Integer.parseInt(txtKolicinaProizvoda.getText());
        kolicinaProizvoda += kolicinaPakiranih;
        
        GotovProizvod proizvod = new GotovProizvod();
        proizvod.setSifraProizvoda(sifraPakiranja);
        proizvod.setKolicinaProizvoda(kolicinaProizvoda);
        sqlproizvod.Izmjena(proizvod);
        
        int sifraPolup = Integer.parseInt(txtSifraPP.getText());
        int potrebnaKolicina = Integer.parseInt(txtPotrebnaKolicina.getText());
        int ukupnoKolicina = potrebnaKolicina * kolicinaPakiranih;
        
        int a = 0;
        int b = 1;
        int kolicinaPolup = 0;
        
        ArrayList<Poluproizvod> listaPoluproizvoda = sqlpp.listaPoluproizvoda();
        for(Poluproizvod pp: listaPoluproizvoda)
        {
            int sifraPP = pp.getSifraPP();
            kolicinaPolup = pp.getKolicinaPP();
            if(sifraPolup == sifraPP)
            {
                if(a<b)
                {
                    kolicinaPolup -= ukupnoKolicina;
                }
                a++;
            }
        }
        
        Poluproizvod pp = new Poluproizvod();
        pp.setSifraPP(sifraPolup);
        pp.setKolicinaPP(kolicinaPolup);
        sqlpp.Izmjena(pp);
        
        btnPrihvatiOdabraniDatum.setVisible(true);
        btnPakiraj.setVisible(false);
        
    }//GEN-LAST:event_btnPakirajActionPerformed

