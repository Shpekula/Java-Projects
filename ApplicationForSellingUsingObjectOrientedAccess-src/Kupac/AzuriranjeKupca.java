    int sifraKupca = Integer.parseInt(txtSifraKupca.getText());
    String nazivKupca = txtNazivKupca.getText();
    String OIBKupca = txtOIBKupca.getText();
    String AdresaKupca = txtAdresaKupca.getText();
    String mailKupca = txtMailKupca.getText();
    Integer KontaktBrojKupca = Integer.parseInt(txtKontakBrojKupca.getText());
    
