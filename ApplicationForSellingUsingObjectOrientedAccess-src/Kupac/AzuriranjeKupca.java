    private void autoinkrementacijaKupca(){
        int sifra = 0;
        ArrayList<Kupac>listaKupaca = sqlKupac.listaKupaca();
        for(Kupac kupac: listaKupaca){
            
            sifra = kupac.getSifraKupca();
        }
        if(sifra == 0){
        sifra = 1;
        txtSifraKupca.setText(Integer.toString(sifra));
        }
        else{
            txtSifraKupca.setText(Integer.toString(sifra+1));
        }
    }
    
