    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        // TODO add your handling code here:
        int sifraProizvoda = Integer.parseInt(txtSifraProizvoda.getText());
        String nazivProizvoda = txtNazivProizvoda.getText();
        float cijenaProizvoda = Float.parseFloat(txtCijenaProizvoda.getText());
        int kolicinaProizvoda = Integer.parseInt(txtKolicinaProizvoda.getText());
        
        Proizvod proizvod = new Proizvod();
        proizvod.setSifraProizvoda(sifraProizvoda);
        proizvod.setNazivProizvoda(nazivProizvoda);
        proizvod.setCijenaProizvoda(cijenaProizvoda);
        proizvod.setKolicinaProizvoda(kolicinaProizvoda);
        
        sqlProizvod.spremi(proizvod);
        
        
        obrisiProizvod();
        popuniProizvod();
        
        txtSifraProizvoda.setText("");
        txtNazivProizvoda.setText("");
        txtCijenaProizvoda.setText("");
        txtKolicinaProizvoda.setText("");
        
        
    }//GEN-LAST:event_btnKreirajActionPerformed

