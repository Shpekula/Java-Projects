 private void autoinkrementacijaProizvoda(){
        int sifra = 0;
        ArrayList<Proizvod>listaProizvoda = sqlProizvod.listaProizvoda();
        for(Proizvod proizvod: listaProizvoda){
            
            sifra = proizvod.getSifraProizvoda();
        }
        if(sifra == 0){
        sifra = 1;
        txtSifraProizvoda.setText(Integer.toString(sifra));
        }
        else{
            txtSifraProizvoda.setText(Integer.toString(sifra+1));
        }
    }
