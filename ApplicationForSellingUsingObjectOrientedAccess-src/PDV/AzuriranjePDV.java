private void autoinkrementacijaRacuna(){
        int sifra = 0;
        ArrayList<PDV>listaPDVa = sqlPDV.listaPDV();
        for(PDV pdv: listaPDVa){
            
            sifra = pdv.getSifraPDV();
        }
        if(sifra == 0){
        sifra = 1;
        txtSifraPDV.setText(Integer.toString(sifra));
        }
        else{
            txtSifraPDV.setText(Integer.toString(sifra+1));
        }
    }
